# defini uma lista
animais = ['gato', 'cachorro', 'leão', 'camelo']
# estrutura de repetição para percorrer a lista
for a in animais:
    print(a, 'Tamanho da string:', len(a))

sequencia = [0, 1, 2, 3, 4, 5]
for num in sequencia:
    print(num)

nome = 'joão victor'
for letra in nome:
    print(letra)

