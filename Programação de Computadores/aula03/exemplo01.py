"""Calculadora simples"""

#Exemplo 01

#Entrada de dados
v1 = float(input('Digite um valor: '))
v2 = float(input('Digite outro valor: '))

#Processamento e saída

print("Soma = ", v1+v2)
print("Subtração = ", v1-v2)
print("Multipicação = ", v1*v2)
print("Divisão = ", v1/v2)
print("Parte inteira da divisão = ", v1//v2)
print("Resto da divisão = ", v1%v2)
print("Exponenciação = ", v1**2)
print("Desconto 5% = ", v1-(v1*0.05))
